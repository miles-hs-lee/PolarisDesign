#!/usr/bin/env node
/**
 * install-git-hook — opt-in pre-push hook that runs `pnpm verify` before
 * every push. Blocks the push if any check fails — same gate as CI but
 * runs locally so you find out before pushing instead of after.
 *
 * Why opt-in (not auto-installed via postinstall):
 *   1. Some teammates prefer pre-commit / no-hooks setups (e.g. running
 *      a fast partial check + relying on CI for full validation).
 *   2. Auto-installing hooks via postinstall is a known-bad pattern —
 *      surprises contributors when `pnpm install` modifies their
 *      git config.
 *   3. Hooks are personal — the right verification cadence (every push
 *      vs every commit vs CI-only) varies per workflow.
 *
 * To opt in:
 *
 *   pnpm verify:install-hook        # writes .git/hooks/pre-push + chmod +x
 *
 * To opt out:
 *
 *   rm .git/hooks/pre-push          # or rename it
 *
 * The hook respects `--no-verify` (git's built-in skip flag) so emergency
 * pushes still work: `git push --no-verify`.
 *
 * If you'd rather skip the slower steps (e2e), set `VERIFY_E2E=0` (default)
 * in your env. To include e2e, set `VERIFY_E2E=1` before pushing or run
 * `pnpm verify --with-e2e` manually before push.
 */
import { writeFileSync, chmodSync, existsSync, mkdirSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, '..');
const hooksDir = resolve(repoRoot, '.git', 'hooks');
const hookPath = resolve(hooksDir, 'pre-push');

if (!existsSync(resolve(repoRoot, '.git'))) {
  console.error('✗ Not a git working tree (no .git/ dir found).');
  console.error('  Run this from the polaris-design repo root after `git clone`.');
  process.exit(1);
}

mkdirSync(hooksDir, { recursive: true });

const hookContent = `#!/usr/bin/env sh
# Auto-generated by scripts/install-git-hook.mjs.
# Runs every CI-equivalent check before push. Same gate as GitHub Actions
# CI workflow — finds breakage before it travels.
#
# Skip with: git push --no-verify
# Re-run / regenerate: pnpm verify:install-hook
# Remove:   rm .git/hooks/pre-push

# Honor the env from the user's shell (e.g. VERIFY_E2E=1).
exec pnpm verify
`;

writeFileSync(hookPath, hookContent, 'utf8');
chmodSync(hookPath, 0o755);

console.log('✓ Installed git pre-push hook → .git/hooks/pre-push');
console.log('  Skip per-push:  git push --no-verify');
console.log('  Run manually:   pnpm verify');
console.log('  With e2e:       pnpm verify --with-e2e');
console.log('  Uninstall:      rm .git/hooks/pre-push');
